import { AbstractControl, FormArray, FormGroup, ValidationErrors, ValidatorFn } from "@angular/forms";

async function sleep() {
    return new Promise(resolve => {
        setTimeout(() => {
            resolve(true)
        }, 2500)
    })
}

export class FormUtils {
    // Expresiones regulares
    static namePattern = '^([a-zA-Z]+) ([a-zA-Z]+)$';
    static emailPattern = '^[a-z0-9._%+-]+@[a-z0-9.-]+\\.[a-z]{2,4}$';
    static notOnlySpacesPattern = '^[a-zA-Z0-9]+$';

    static getTextError(errors: ValidationErrors) {
        for (const key of Object.keys(errors)) {
            switch (key) {
                case 'required':
                    return 'Este campo es requerido'
                case 'minlength':
                    return `Mínimo de ${errors['minlength'].requiredLength} caracteres.`
                case 'min':
                    return `Valor mínimo de ${errors['min'].min} caracteres.`
                case 'email':
                    return `El valor ingresado no es un correo electrónico.`
                case 'emailTaken':
                    return `El correo electronico ya est á siendo usado por otro usuario.`
                case 'noStrider':
                    return `No se puede colocar ese nombre, por que ya esta siendo usado por otro usuario.`

                case 'pattern':
                    {
                        if (errors['pattern'].requiredPattern === FormUtils.namePattern)
                            return 'El campo debe de tener nombre y apellido'
                        if (errors['pattern'].requiredPattern === FormUtils.emailPattern)
                            return 'El valor ingresado no luce como un correo electrónico.'
                        return 'Error de patrón contra expresión regular.'
                    }

                default:
                    return `Error de validación no controlado ${key}.`
            }
        }
        return null
    }

    static isValidField(form: FormGroup, fieldName: string): boolean | null {
        return !!form.controls[fieldName].errors && form.controls[fieldName].touched
    }

    static isValidFieldInArray(formArray: FormArray, index: number) {
        return formArray.controls[index].errors && formArray.controls[index].touched
    }

    static getFieldError(form: FormGroup, fieldName: string): string | null {
        if (!form.controls[fieldName]) return null
        const errors = form.controls[fieldName].errors ?? {}
        return FormUtils.getTextError(errors)
    }

    static getFieldErrorInArray(formArray: FormArray, index: number) {
        if (formArray.controls.length === 0) return null
        const errors = formArray.controls[index].errors ?? {}
        return FormUtils.getTextError(errors)
    }

    static isFieldOneEqualFieldTwo(field1: string, field2: string): ValidatorFn {
        return (formGroup: AbstractControl): ValidationErrors | null => {
            const field1Value = formGroup.get(field1)?.value
            const field2Value = formGroup.get(field2)?.value
            return field1Value === field2Value ? null : { fieldsAreNotEqual: true }
        }
    }

    static async checkingServerResponse(control: AbstractControl): Promise<ValidationErrors | null> {
        await sleep();
        const formValue = control.value;
        if (formValue === "hola@mundo.com") {
            return {
                emailTaken: true
            }
        }
        return null
    }

    static notStrider(control: AbstractControl): ValidationErrors | null {
        const formValue = control.value;
        if (formValue === "strider") {
            return {
                noStrider: true
            }
        }
        return null
    }

}